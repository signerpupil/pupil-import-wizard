/**
 * Normalize an Ort string for fuzzy comparison.
 * Lowercases, trims, removes common suffixes like ZH, BE, etc.
 */
function normalizeOrt(ort: string): string {
  return ort
    .trim()
    .toLowerCase()
    // Remove canton suffixes like "ZH", "BE", "SG", etc.
    .replace(/\s+(zh|be|lu|ur|sz|ow|nw|gl|zg|fr|so|bs|bl|sh|ar|ai|sg|gr|ag|tg|ti|vd|vs|ne|ge|ju|fl)\s*$/i, '')
    // Remove parenthetical like "(Gossau ZH)"
    .replace(/\s*\(.*?\)\s*/g, '')
    .replace(/\s+/g, ' ')
    .trim();
}

/**
 * Check if a PLZ-Ort combination is valid.
 * Returns: 
 *   - null if PLZ is unknown (no validation possible)
 *   - true if the Ort matches
 *   - string[] of expected Ort names if mismatch
 */
export function validatePlzOrt(plz: string, ort: string): null | true | string[] {
  const plzClean = String(plz).trim();
  const expectedOrte = SWISS_PLZ_ORT[plzClean];

  if (!expectedOrte) return null; // Unknown PLZ, can't validate

  const normalizedInput = normalizeOrt(ort);

  for (const expected of expectedOrte) {
    if (normalizeOrt(expected) === normalizedInput) {
      return true;
    }
  }

  return expectedOrte;
}

/**
 * Get the expected Ort(e) for a PLZ.
 * Returns null if PLZ is not in the database.
 */
export function getOrteForPlz(plz: string): string[] | null {
  return SWISS_PLZ_ORT[String(plz).trim()] ?? null;
}
